#!/bin/bash
# ============================================================
# setup.sh - 開発環境セットアップスクリプト
# ============================================================

set -e  # エラー時に停止

echo "🚀 開発環境をセットアップしています..."
echo ""

# ============================================================
# 1. 必須ツールの確認
# ============================================================
echo "📋 必須ツールを確認しています..."

check_command() {
  if ! command -v "$1" &> /dev/null; then
    echo "❌ $1 がインストールされていません。インストールしてください。"
    echo "   参考: $2"
    exit 1
  else
    echo "✅ $1: $(command -v $1)"
  fi
}

check_command "git" "https://git-scm.com/"
check_command "node" "https://nodejs.org/"
check_command "npm" "https://nodejs.org/"
# check_command "python3" "https://www.python.org/"
# check_command "gcloud" "https://cloud.google.com/sdk/docs/install"

echo ""

# ============================================================
# 2. Node.jsバージョン確認
# ============================================================
NODE_VERSION=$(node -v | cut -d'v' -f2 | cut -d'.' -f1)
if [ "$NODE_VERSION" -lt 18 ]; then
  echo "❌ Node.js 18以上が必要です（現在: $(node -v)）"
  exit 1
fi
echo "✅ Node.js バージョン: $(node -v)"

# ============================================================
# 3. 環境変数ファイルの作成
# ============================================================
if [ ! -f ".env" ]; then
  echo ""
  echo "📝 .env ファイルを作成しています..."
  cp .env.example .env
  echo "✅ .env を作成しました。値を設定してください: .env"
else
  echo "✅ .env ファイルが既に存在します"
fi

# ============================================================
# 4. 依存パッケージのインストール
# ============================================================
# echo ""
# echo "📦 依存パッケージをインストールしています..."
# npm install
# echo "✅ パッケージインストール完了"

# ============================================================
# 5. 完了
# ============================================================
echo ""
echo "============================================"
echo "✅ セットアップが完了しました！"
echo ""
echo "次のステップ:"
echo "  1. .env ファイルに必要な値を設定する"
echo "  2. docs/mcp-setup.md を読んでMCPを設定する"
echo "  3. make dev で開発サーバーを起動する"
echo "============================================"
